// Helper functions
export const getAllDinosaurs = () => {
  return [
    ...dinosaurDatabase.carnivores,
    ...dinosaurDatabase.herbivores,
    ...dinosaurDatabase.aquatics,
    ...dinosaurDatabase.flyers,
    ...dinosaurDatabase.omnivores
  ];
};

export const getDinosaursByCategory = (category) => {
  switch(category.toLowerCase()) {
    case 'carnivore': return dinosaurDatabase.carnivores;
    case 'herbivore': return dinosaurDatabase.herbivores;
    case 'aquatic': return dinosaurDatabase.aquatics;
    case 'flyer': return dinosaurDatabase.flyers;
    case 'omnivore': return dinosaurDatabase.omnivores;
    default: return getAllDinosaurs();
  }
};

export const getDinosaursByRarity = (rarity) => {
  return getAllDinosaurs().filter(dino => dino.rarity === rarity);
};

export const searchDinosaurs = (query) => {
  const searchTerm = query.toLowerCase();
  return getAllDinosaurs().filter(dino => 
    dino.name.toLowerCase().includes(searchTerm) ||
    dino.abilities.toLowerCase().includes(searchTerm) ||
    dino.category.toLowerCase().includes(searchTerm)
  );
};